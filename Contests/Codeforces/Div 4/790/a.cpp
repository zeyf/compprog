/*

Link: https://codeforces.com/contest/1676/problem/A

Topic: Implementation

Approach: Simple if check.

Time Complexity: O(1)
Space Complexity: O(1)

*/

int main () {
    int n; cin >> n;

    // n cases
    while (n--) {
        // read string, check sums
        string s; cin >> s;
        if (s[0]+s[1]+s[2]-48*3 == s[3]+s[4]+s[5]-48*3)
            cout << "YES\n";
        else
            cout << "NO\n";
    }
    return 0;
};